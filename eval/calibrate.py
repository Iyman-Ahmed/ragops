"""Evaluate the eval — measure the gate's true/false positive rate.

A gate you can't trust is worse than no gate. This harness injects known regressions and
confirms the retrieval gate (L1) FIRES on retrieval-quality regressions and stays GREEN on
a clean run and on changes that are out of its scope (which are caught by L2/L3 instead).
The result is a measured TPR/FPR table written to docs/gate_calibration.md.

Run:  python -m eval.calibrate
"""
from __future__ import annotations

import pathlib
import sys
from dataclasses import dataclass

from eval.harness import RunConfig, run_retrieval
from eval.retrieval_gate import THRESHOLDS

DOCS = pathlib.Path(__file__).parent.parent / "docs"


@dataclass
class Case:
    name: str
    cfg: RunConfig
    should_fire: bool
    note: str


CASES = [
    Case("baseline (no change)", RunConfig(), False,
         "healthy pipeline — gate must stay green"),
    Case("embedding swap -> hash-64", RunConfig(embed_model="hash-64"), True,
         "weaker embedding ranks gold below rank 1"),
    Case("embedding collapse -> hash-8", RunConfig(embed_model="hash-8"), True,
         "severe hash collisions destroy ranking"),
    Case("corpus truncation (4 of 10 docs)", RunConfig(max_docs=4), True,
         "documents lost at ingest — gold no longer retrievable"),
    Case("top_k reduced 4 -> 1", RunConfig(top_k=1), True,
         "fewer chunks retrieved -> recall@k drops 1.00 to 0.84; caught by the recall@k floor"),
    Case("over-fragmented chunks (50 chars)", RunConfig(chunk_chars=50), False,
         "OUT OF L1 SCOPE: rank-1 recall holds; harm shows up in generation/latency (L3)"),
]


def _fires(metrics: dict) -> list[str]:
    return [k for k, thr in THRESHOLDS.items() if metrics[k] < thr]


def main() -> int:
    rows = []
    tp = fp = fn = tn = 0
    for c in CASES:
        m = run_retrieval(c.cfg)
        fired_on = _fires(m)
        fired = bool(fired_on)
        ok = fired == c.should_fire
        tp += fired and c.should_fire
        fp += fired and not c.should_fire
        fn += (not fired) and c.should_fire
        tn += (not fired) and not c.should_fire
        rows.append((c, m, fired, fired_on, ok))

    should_fire_n = sum(c.should_fire for c in CASES)
    should_not_n = len(CASES) - should_fire_n
    tpr = tp / should_fire_n if should_fire_n else 0.0
    fpr = fp / should_not_n if should_not_n else 0.0

    lines = [
        "# Gate calibration — evaluating the eval",
        "",
        "Generated by `python -m eval.calibrate`. Confirms the deterministic retrieval "
        "gate (L1) fires on retrieval-quality regressions and stays green otherwise.",
        "",
        f"**True-positive rate: {tp}/{should_fire_n} = {tpr:.0%}**  ·  "
        f"**False-positive rate: {fp}/{should_not_n} = {fpr:.0%}**",
        "",
        f"Thresholds: `{THRESHOLDS}`",
        "",
        "| injected change | recall@1 | recall@1 Wilson LB | recall@k | gate | "
        "expected | verdict |",
        "|---|---|---|---|---|---|---|",
    ]
    for c, m, fired, _fired_on, ok in rows:
        gate = "🔴 FAIL" if fired else "🟢 pass"
        exp = "fire" if c.should_fire else "stay green"
        lines.append(
            f"| {c.name} | {m['recall_at_1']:.3f} | {m['recall_at_1_wilson_lb']:.3f} | "
            f"{m['recall_at_k']:.3f} | {gate} | {exp} | {'✓' if ok else '✗ MISCALIBRATED'} |")
    lines += ["", "Notes:", ""]
    for c in CASES:
        lines.append(f"- **{c.name}** — {c.note}")
    lines.append("")

    DOCS.mkdir(exist_ok=True)
    (DOCS / "gate_calibration.md").write_text("\n".join(lines))
    print("\n".join(lines))

    if tpr < 1.0 or fpr > 0.0:
        print(f"\nCALIBRATION FAILED: TPR={tpr:.0%} FPR={fpr:.0%} "
              "(gate no longer catches what it should)", file=sys.stderr)
        return 1
    print(f"\nCALIBRATION OK: TPR={tpr:.0%} FPR={fpr:.0%}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
